// == , != ====> value
// === , !== ===> value and type

console.log(5 == '5')
console.log(5 === '5')
console.log(7 !== '7')
console.log(7 !== 7)
console.log(7 != '7')
console.log(7 === 7)

// Logical operator

// AND  &&

//true && true  ===> true
//true && false ===> false
//false && true ===> false
//false && false ===> false

console.log(7 === 7 && 7 !== '7')
//            true  &&  true
console.log(8 == '8' && 7 < 6)
//            true   &&  false  
console.log(7 != 7 && 8 > 4)
//           false && true    
console.log(7 !== 7 && 8 != '8') 
//           false  &&  false  

// OR   ||

//true  ||  true ====> true
//false || true  ====>  true 
//true  || false ====>  true
//false || false  ===>  false

console.log(7 === 7 || 8 === 8)
//           true   ||  true  ====> true
console.log(6 < 7 || 7 > 8)
//           true || false
console.log(7 !== 7 || 8 >= 8)
//           false  ||  true
console.log(6 < 2 || 8 < 5)
//           false || false  

// NOT
// true ===> false
// false ===> true
console.log(!7 === 7)
console.log(!7 !== 7)


